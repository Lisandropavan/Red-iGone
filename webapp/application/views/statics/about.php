  		<h1>About Red iGone</h1>

  		<h2>What is Red iGone?</h2>
  		<p>The idea with Red iGone is dead simple. Anyone should be able to remove red-eyes from digital photos. You shouldn't need a degree in computer science or read a book on how to use Photoshop.</p>

  		<h2>How it all got started</h2>
  		<p>It all started back in the summer of 2009. Stefan had just temporarily moved back to his hometown in Sweden after living in Japan. Coincidentally, Viktor was also back in Sweden after living in the US (Silicon Valley to be precise). For a few years the two had been talking about combining their talents and develop a web app. Since Stefan is a programmer and Viktor is a Biz. dev/sysadmin (strange combination, isn't it?) the two makes a pretty good team.</p>

  		<p>After brainstorming over quite a few lattes, Stefan came up with the idea of a web app that removes red eyes in photos. Red-eye removal is hardly anything new, but it turned out to be a great space. All the current tools on the market were either expensive or simply hard to use (in some cases, both).</p>

  		<p>Over the course of the next few weeks, they started developing a prototype. As most prototypes, it was ugly and full of bugs. Yet, it did work. Now, as those of you who have been involved with developing any kind of software knows, the path from prototype to a fully working product or service is long and bumpy.</p>

  		<p>The following months, Stefan got busy writing the code for the front-end and back-end, while Viktor got busy designing the website and the server architecture.</p>

  		<p>As you can see, all the hard work payed off. Red iGone is dead simple to use and, we think the site is looking pretty awesome too!</p>

  		<h2>What's next?</h2>
  		<p>We cannot really reveal a whole lot, but let's just say that APIs are pretty hot.</p>

  		<p>Follow us on <a href="http://twitter.com/redigone" target="_blank">Twitter (@redigone)</a> for the latest updates.</p>
