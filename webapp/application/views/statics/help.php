    <p>
    <a name="what_is_redigone" class="orange">What is RediGone?</a><br />
    Red iGone is simply the world's easiest red-eye removal tool. The only thing you need is a web-browser and your photos. You won't even have to download anything. Everything is handled directly on our servers.
    </p>

    <p><a name="its_not_working" class="orange">Help! It's not working!</a><br />
    Hold on, we cannot help you unless we know exactly where and how the problem happened. If the problem keeps occurring, please use the 'Feedback' button to the left and report the error. Please include the page it happened on, and what browser you are using.
    </p>

    <p><a name="supported_browsers" class="orange">What browsers are supported by RediGone?</a><br />
    RediGone should be working in all modern browsers (Internet Explorer, Firefox, Safari etc.). The only browser we do not officially support is Internet Explorer 6.0 and earlier. We have no intention of adding support for IE6 and instead urge you to upgrade from this decade.
    </p>

    <p><a name="blue" class="orange">I really don't like the blueish color you're using. Can you change it please?</a><br />
    No.
    </p>

    <p><a name="share" class="orange">I love your service! How can I share it with all my friends?</a><br />
    Awesome! The easiest way is to head over to our <a href="/share">share-page</a> where we have sharing-tools set up for most social networks and bookmarking sites.
    </p>

    <p><a name="supported_formats" class="orange">What image formats are supported?</a><br />
    We support most common file-formats for images, such as BMP, JPG, PNG and TIF.
    </p>

    <p><a name="image_size" class="orange">How big can my photos be?</a><br />
    Right now we do not allow photos larger than 2Mb.
    </p>

